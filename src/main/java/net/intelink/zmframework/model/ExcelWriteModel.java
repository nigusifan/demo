package net.intelink.zmframework.model;


import java.util.ArrayList;
import java.util.List;

/**
 * Excel操作对象
 * @author feng
 *
 */
public class ExcelWriteModel {
	private int startRowIndex = 0;

	private int startLeftIndex = 0;

	private int columnLen = 1;

	private String ItemPropertyName = "";

	private List<ExcelItemModel> itemModel = null;

	private List<ExcelItemModel> itemTitleList = null;

	private List<ExcelItemModel> mainWriteModelList = null;

	private String excellSheelName = "sheel";

	private String mainTitle = "";
	private Class classs;

	public int getColumnLen() {
		return this.columnLen;
	}

	public void setColumnLen(int columnLen) {
		this.columnLen = columnLen;
	}

	public List<ExcelItemModel> getItemModel() {
		if (this.itemModel == null)
			this.itemModel = new ArrayList<>();
		return this.itemModel;
	}

	public void setItemModel(List<ExcelItemModel> itemModel) {
		this.itemModel = itemModel;
	}

	public int getStartLeftIndex() {
		return this.startLeftIndex;
	}

	public void setStartLeftIndex(int startLeftIndex) {
		this.startLeftIndex = startLeftIndex;
	}

	public int getStartRowIndex() {
		return this.startRowIndex;
	}

	public void setStartRowIndex(int startRowIndex) {
		this.startRowIndex = startRowIndex;
	}

	public Class getClasss() {
		return this.classs;
	}

	public void setClasss(Class classs) {
		this.classs = classs;
	}

	public String getItemPropertyName() {
		return this.ItemPropertyName;
	}

	public void setItemPropertyName(String itemPropertyName) {
		this.ItemPropertyName = itemPropertyName;
	}

	public List<ExcelItemModel> getMainWriteModelList() {
		if (this.mainWriteModelList == null)
			this.mainWriteModelList = new ArrayList<>();
		return this.mainWriteModelList;
	}

	public void setMainWriteModelList(List<ExcelItemModel> mainWriteModelList) {
		this.mainWriteModelList = mainWriteModelList;
	}

	public String getExcellSheelName() {
		return this.excellSheelName;
	}

	public void setExcellSheelName(String excellSheelName) {
		this.excellSheelName = excellSheelName;
	}

	public List<ExcelItemModel> getItemTitleList() {
		if (this.itemTitleList == null)
			this.itemTitleList = new ArrayList<>();
		return this.itemTitleList;
	}

	public void setItemTitleList(List<ExcelItemModel> itemTitleList) {
		this.itemTitleList = itemTitleList;
	}

	public String getMainTitle() {
		return this.mainTitle;
	}

	public void setMainTitle(String mainTitle) {
		this.mainTitle = mainTitle;
	}
}